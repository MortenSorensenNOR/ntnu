def convert_index_data(input_data):
    # Split the input data into lines
    lines = input_data.strip().split('\n')
    
    # Initialize an empty list to store the output lines
    output_lines = []
    
    # Iterate through each line
    for line in lines:
        # Split each line into components
        components = line.split()
        
        # Check if the line starts with 'f'
        if components[0] == 'f':
            # Extract the first number from each x/y/z group and subtract 1
            x1 = int(components[1].split('/')[0]) - 1
            x2 = int(components[2].split('/')[0]) - 1
            x3 = int(components[3].split('/')[0]) - 1
            
            # Format the line as required
            formatted_line = f".word {x1}, {x2}, {x3}, 0"
            
            # Add the formatted line to the output list
            output_lines.append(formatted_line)
    
    # Join the output lines into a single string separated by newlines
    return '\n'.join(output_lines)

def convert_vertex_data(input_data):
    # Split the input data into lines
    lines = input_data.strip().split('\n')
    
    # Initialize an empty list to store the output lines
    output_lines = []
    
    # Iterate through each line
    for line in lines:
        # Split each line into components
        components = line.split()
        
        # Check if the line starts with 'v'
        if components[0] == 'v':
            # Extract the three float values
            x, y, z = components[1], components[2], components[3]
            
            # Format the line as required
            formatted_line = f".float {x}, {y}, {z}, 1.0"
            
            # Add the formatted line to the output list
            output_lines.append(formatted_line)
    
    # Join the output lines into a single string separated by newlines
    return '\n'.join(output_lines)

